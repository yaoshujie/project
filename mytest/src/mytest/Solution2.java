	private int leftmaxindex(int[] B, int i) {
		int k = i;
		int max_id =k;
		int leftmax = B[k];
		while (k >= 0) {
			if (B[k] > leftmax) {
				leftmax = B[k];
				max_id =k;
			}
			k = k - 1;
		}
		return max_id;		
	}
	
	private int rightmaxindex(int[] B, int i, int leftmax) {
		int j = i + 1;
		int max_id =j;
		if(max_id>=B.length)
		{
			return max_id;
		}
		int rightmax = B[j];
		while (j < B.length) {
			if (B[j] > rightmax) {
				rightmax = B[j];
				max_id =j;
			}
			if(rightmax >=leftmax)
			{
				return max_id;
			}
			j = j + 1;
		}
		return max_id;
	}

